# パターンマッチング機能
function Find-ErrorPattern {
    param([string]$ErrorMessage, [string]$ErrorType = "")
    
    Write-ErrorLog "エラーパターンをマッチング中..." "INFO"
    
    $patternFile = Join-Path $PatternsPath "error-patterns.json"
    if (-not (Test-Path $patternFile)) {
        Write-ErrorLog "パターンファイルが見つかりません" "WARNING"
        return $null
    }
    
    try {
        $patterns = Get-Content $patternFile | ConvertFrom-Json -AsHashtable
    } catch {
        Write-ErrorLog "パターンファイルの読み込みに失敗しました" "ERROR"
        return $null
    }
    
    # 類似パターンを検索
    $matches = @()
    
    foreach ($patternKey in $patterns.Keys) {
        $pattern = $patterns[$patternKey]
        
        # エラータイプが一致する場合は高スコア
        $score = 0
        if ($ErrorType -and $pattern.ErrorType -eq $ErrorType) {
            $score += 50
        }
        
        # エラーメッセージの類似度チェック（簡単な文字列マッチング）
        foreach ($example in $pattern.Examples) {
            $similarity = Get-StringSimilarity $ErrorMessage $example.Message
            if ($similarity -gt 0.3) {
                $score += ($similarity * 30)
            }
        }
        
        if ($score -gt 20) {  # 閾値
            $matches += @{
                Pattern = $pattern
                PatternKey = $patternKey
                Score = $score
                Confidence = [math]::Round($score / 80 * 100, 1)  # パーセンテージ
            }
        }
    }
    
    # スコア順でソート
    $matches = $matches | Sort-Object Score -Descending | Select-Object -First 3
    
    if ($matches.Count -gt 0) {
        Write-ErrorLog "パターンマッチング完了: $($matches.Count)件の候補を発見" "SUCCESS"
        return $matches
    } else {
        Write-ErrorLog "マッチするパターンが見つかりませんでした" "INFO"
        return $null
    }
}

# 文字列類似度計算（簡易版）
function Get-StringSimilarity {
    param([string]$String1, [string]$String2)
    
    if ([string]::IsNullOrEmpty($String1) -or [string]::IsNullOrEmpty($String2)) {
        return 0
    }
    
    # Levenshtein距離ベースの簡易計算
    $longer = if ($String1.Length -gt $String2.Length) { $String1 } else { $String2 }
    $shorter = if ($String1.Length -le $String2.Length) { $String1 } else { $String2 }
    
    if ($longer.Length -eq 0) { return 1 }
    
    # 簡易的な類似度計算
    $commonChars = 0
    for ($i = 0; $i -lt $shorter.Length; $i++) {
        if ($i -lt $longer.Length -and $shorter[$i] -eq $longer[$i]) {
            $commonChars++
        }
    }
    
    return $commonChars / $longer.Length
}

# リカバリ推奨機能
function Get-RecoveryRecommendation {
    param([string]$ErrorMessage, [string]$ErrorType, [string]$File)
    
    Write-ErrorLog "リカバリ推奨を生成中..." "INFO"
    
    # パターンマッチング結果を取得
    $matches = Find-ErrorPattern $ErrorMessage $ErrorType
    
    $recommendations = @{
        ImmediateActions = @()
        Solutions = @()
        PreventionTips = @()
        EstimatedTime = "不明"
        Confidence = 0
    }
    
    if ($matches -and $matches.Count -gt 0) {
        $bestMatch = $matches[0]
        $recommendations.Confidence = $bestMatch.Confidence
        $recommendations.EstimatedTime = "$($bestMatch.Pattern.AverageTimeMinutes)分"
        
        # 過去の解決策を提案
        foreach ($solution in $bestMatch.Pattern.CommonSolutions) {
            $recommendations.Solutions += $solution
        }
    }
    
    # エラータイプ別の一般的な推奨事項
    switch ($ErrorType) {
        "syntax" {
            $recommendations.ImmediateActions += "lake check でファイル全体の構文をチェック"
            $recommendations.ImmediateActions += "Lean Language Server の診断メッセージを確認"
            $recommendations.PreventionTips += "コミット前に必ず構文チェックを実行"
        }
        "type" {
            $recommendations.ImmediateActions += "型注釈を明示的に追加"
            $recommendations.ImmediateActions += "#check コマンドで型を確認"
            $recommendations.PreventionTips += "複雑な式には型注釈を付ける習慣を"
        }
        "tactic" {
            $recommendations.ImmediateActions += "tactic state を確認"
            $recommendations.ImmediateActions += "より基本的な戦術に分解"
            $recommendations.PreventionTips += "戦術の適用前にgoalを明確にする"
        }
        "import" {
            $recommendations.ImmediateActions += "lake update で依存関係を更新"
            $recommendations.ImmediateActions += "import文の順序を確認"
            $recommendations.PreventionTips += "必要最小限のimportのみを行う"
        }
        "mathlib" {
            $recommendations.ImmediateActions += "Mathlib バージョンを確認"
            $recommendations.ImmediateActions += "lake clean && lake build で再ビルド"
            $recommendations.PreventionTips += "Mathlib更新時は段階的に適用"
        }
    }
    
    Write-ErrorLog "リカバリ推奨を生成しました (信頼度: $($recommendations.Confidence)%)" "SUCCESS"
    
    return $recommendations
}

# メイン実行ロジック
switch ($Action) {
    "record" {
        if (-not $ProofFile -or -not $ErrorType) {
            Write-ErrorLog "必須パラメータが不足しています: -ProofFile, -ErrorType" "ERROR"
            exit 1
        }
        
        $record = Record-ErrorFix -File $ProofFile -Type $ErrorType -Message $ErrorMessage -Solution $Solution -Category $Category -TimeSpent $TimeSpent
        Write-Host "`n✅ エラー修正記録を作成しました" -ForegroundColor Green
        Write-Host "記録ID: $($record.Id)"
        Write-Host "ファイル: $($record.File)"
        Write-Host "カテゴリ: $($record.Category)"
    }
    
    "analyze" {
        $analysis = Analyze-Errors $Days
        Write-Host "`n📊 エラー分析結果 (過去 $Days 日間)" -ForegroundColor Cyan
        Write-Host "総エラー数: $($analysis.TotalErrors)"
        Write-Host "平均解決時間: $($analysis.AverageResolutionTime)分"
        Write-Host "`nカテゴリ別:"
        $analysis.ByCategory | ForEach-Object { Write-Host "  $($_.Category): $($_.Count)件" }
    }
    
    "report" {
        $reportFile = New-ErrorReport $Days
        Write-Host "`n📄 エラー分析レポートを生成しました" -ForegroundColor Green
        Write-Host "ファイル: $reportFile"
    }
    
    "recovery" {
        if (-not $ErrorMessage) {
            Write-ErrorLog "エラーメッセージが必要です: -ErrorMessage" "ERROR"
            exit 1
        }
        
        $recommendation = Get-RecoveryRecommendation $ErrorMessage $ErrorType $ProofFile
        Write-Host "`n🔧 リカバリ推奨事項" -ForegroundColor Cyan
        Write-Host "信頼度: $($recommendation.Confidence)%"
        Write-Host "推定時間: $($recommendation.EstimatedTime)"
        Write-Host "`n即座の対応:"
        $recommendation.ImmediateActions | ForEach-Object { Write-Host "  - $_" }
        Write-Host "`n推奨解決策:"
        $recommendation.Solutions | ForEach-Object { Write-Host "  - $_" }
    }
    
    "pattern-match" {
        if (-not $ErrorMessage) {
            Write-ErrorLog "エラーメッセージが必要です: -ErrorMessage" "ERROR"
            exit 1
        }
        
        $matches = Find-ErrorPattern $ErrorMessage $ErrorType
        if ($matches) {
            Write-Host "`n🔍 パターンマッチング結果" -ForegroundColor Cyan
            foreach ($match in $matches) {
                Write-Host "パターン: $($match.PatternKey) (信頼度: $($match.Confidence)%)"
                Write-Host "  発生回数: $($match.Pattern.Count)"
                Write-Host "  推奨解決策: $($match.Pattern.CommonSolutions -join ', ')"
                Write-Host ""
            }
        } else {
            Write-Host "マッチするパターンが見つかりませんでした" -ForegroundColor Yellow
        }
    }
    
    default {
        Write-Host "不明なアクション: $Action" -ForegroundColor Red
        Write-Host "使用可能なアクション: record, analyze, report, recovery, pattern-match"
        exit 1
    }
}