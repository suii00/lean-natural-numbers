# Lean証明エラー修正履歴記録システム
# Version: 1.0.0

[CmdletBinding()]
param(
    [Parameter(Mandatory=$true)]
    [ValidateSet("record", "analyze", "report", "recovery", "pattern-match")]
    [string]$Action,
    
    [string]$ProofFile = "",
    [string]$ErrorType = "",
    [string]$ErrorMessage = "",
    [string]$Solution = "",
    [string]$TimeSpent = "",
    [ValidateSet("syntax", "type", "tactic", "import", "mathlib", "logic", "performance", "other")]
    [string]$Category = "other",
    [int]$Days = 7
)

# 設定とパス
$WorkflowRoot = Split-Path -Parent (Split-Path -Parent $PSScriptRoot)
$ErrorLogsPath = Join-Path $WorkflowRoot "error-tracking\logs"
$PatternsPath = Join-Path $WorkflowRoot "error-tracking\patterns"
$RecoveryPath = Join-Path $WorkflowRoot "error-tracking\recovery"

# ディレクトリ確保
@($ErrorLogsPath, $PatternsPath, $RecoveryPath) | ForEach-Object {
    if (-not (Test-Path $_)) {
        New-Item -Path $_ -ItemType Directory -Force | Out-Null
    }
}

# ログ記録関数
function Write-ErrorLog {
    param([string]$Message, [string]$Level = "INFO")
    $timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
    $logEntry = "[$timestamp] [$Level] $Message"
    
    switch ($Level) {
        "ERROR" { Write-Host $logEntry -ForegroundColor Red }
        "WARNING" { Write-Host $logEntry -ForegroundColor Yellow }
        "SUCCESS" { Write-Host $logEntry -ForegroundColor Green }
        "INFO" { Write-Host $logEntry -ForegroundColor Cyan }
        default { Write-Host $logEntry -ForegroundColor White }
    }
    
    $logFile = Join-Path $ErrorLogsPath "error-tracker-$(Get-Date -Format 'yyyy-MM-dd').log"
    $logEntry | Out-File -FilePath $logFile -Append -Encoding UTF8
}

# エラー記録機能
function Record-ErrorFix {
    param(
        [string]$File,
        [string]$Type,
        [string]$Message,
        [string]$Solution,
        [string]$Category,
        [string]$TimeSpent
    )
    
    Write-ErrorLog "エラー修正記録を作成中..." "INFO"
    
    # 現在のGit情報取得
    $gitInfo = @{
        CommitBefore = ""
        CommitAfter = ""
        Branch = ""
        FileDiff = ""
    }
    
    try {
        $gitInfo.CommitBefore = & git rev-parse HEAD 2>$null
        $gitInfo.Branch = & git branch --show-current 2>$null
        $gitInfo.FileDiff = & git diff HEAD -- $File 2>$null
    } catch {
        Write-ErrorLog "Git情報の取得に部分的に失敗しました" "WARNING"
    }
    
    # エラーレコード作成
    $errorRecord = [PSCustomObject]@{
        Id = [guid]::NewGuid().ToString().Substring(0,8)
        Timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
        File = $File
        ErrorType = $Type
        ErrorMessage = $Message
        Solution = $Solution
        Category = $Category
        TimeSpentMinutes = if($TimeSpent) { [int]$TimeSpent } else { $null }
        GitInfo = $gitInfo
        Environment = @{
            LeanVersion = (& lean --version 2>$null) -replace "Lean \(version ([^,]+).*", '$1'
            LakeVersion = (& lake --version 2>$null)
            Platform = $env:OS
            Timestamp = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
        }
        Resolution = @{
            Status = "Fixed"
            VerificationPassed = $false
            AutoGenerated = $false
        }
    }
    
    # 検証実行
    if (Test-Path $File) {
        Write-ErrorLog "修正後の検証を実行中..." "INFO"
        $verification = & lake check $File 2>&1
        $errorRecord.Resolution.VerificationPassed = ($LASTEXITCODE -eq 0)
        
        if ($errorRecord.Resolution.VerificationPassed) {
            $gitInfo.CommitAfter = & git rev-parse HEAD 2>$null
            Write-ErrorLog "検証成功: 修正が確認されました" "SUCCESS"
        } else {
            Write-ErrorLog "検証失敗: まだエラーが残っています" "WARNING"
            $errorRecord.Resolution.Status = "Partial"
        }
    }
    
    # レコード保存
    $monthlyLogFile = Join-Path $ErrorLogsPath "error-fixes-$(Get-Date -Format 'yyyy-MM').json"
    
    $existingRecords = @()
    if (Test-Path $monthlyLogFile) {
        try {
            $existingRecords = Get-Content $monthlyLogFile | ConvertFrom-Json
        } catch {
            Write-ErrorLog "既存ログファイルの読み込みに失敗しました" "WARNING"
        }
    }
    
    $allRecords = @($existingRecords) + @($errorRecord)
    $allRecords | ConvertTo-Json -Depth 4 | Out-File -FilePath $monthlyLogFile -Encoding UTF8
    
    Write-ErrorLog "エラー修正記録を保存しました: ID $($errorRecord.Id)" "SUCCESS"
    
    # パターン学習
    Update-ErrorPattern $errorRecord
    
    return $errorRecord
}

# エラーパターン更新
function Update-ErrorPattern {
    param([PSCustomObject]$ErrorRecord)
    
    $patternFile = Join-Path $PatternsPath "error-patterns.json"
    
    $patterns = @{}
    if (Test-Path $patternFile) {
        try {
            $patterns = Get-Content $patternFile | ConvertFrom-Json -AsHashtable
        } catch {
            Write-ErrorLog "パターンファイルの読み込みに失敗しました" "WARNING"
        }
    }
    
    # パターンキー生成
    $patternKey = "$($ErrorRecord.Category)-$($ErrorRecord.ErrorType)"
    
    if (-not $patterns.ContainsKey($patternKey)) {
        $patterns[$patternKey] = @{
            Category = $ErrorRecord.Category
            ErrorType = $ErrorRecord.ErrorType
            Count = 0
            CommonSolutions = @()
            AverageTimeMinutes = 0
            LastSeen = ""
            Examples = @()
        }
    }
    
    # パターン更新
    $pattern = $patterns[$patternKey]
    $pattern.Count++
    $pattern.LastSeen = $ErrorRecord.Timestamp
    
    # 解決策を追加
    if ($ErrorRecord.Solution -and $ErrorRecord.Solution -notin $pattern.CommonSolutions) {
        $pattern.CommonSolutions += $ErrorRecord.Solution
        # 最大5つまで保持
        if ($pattern.CommonSolutions.Count -gt 5) {
            $pattern.CommonSolutions = $pattern.CommonSolutions | Select-Object -Last 5
        }
    }
    
    # 平均時間更新
    if ($ErrorRecord.TimeSpentMinutes) {
        $pattern.AverageTimeMinutes = [math]::Round(($pattern.AverageTimeMinutes + $ErrorRecord.TimeSpentMinutes) / 2, 1)
    }
    
    # 例を追加
    $example = @{
        File = $ErrorRecord.File
        Message = $ErrorRecord.ErrorMessage
        Solution = $ErrorRecord.Solution
        Date = $ErrorRecord.Timestamp
    }
    
    $pattern.Examples += $example
    if ($pattern.Examples.Count -gt 3) {
        $pattern.Examples = $pattern.Examples | Select-Object -Last 3
    }
    
    # パターンファイル保存
    $patterns | ConvertTo-Json -Depth 4 | Out-File -FilePath $patternFile -Encoding UTF8
    
    Write-ErrorLog "エラーパターンを更新しました: $patternKey" "INFO"
}

# エラー分析機能
function Analyze-Errors {
    param([int]$Days)
    
    Write-ErrorLog "$Days 日間のエラー分析を実行中..." "INFO"
    
    $analysisResults = @{
        Period = "$Days days"
        TotalErrors = 0
        ByCategory = @{}
        ByFile = @{}
        TopErrors = @()
        AverageResolutionTime = 0
        TrendAnalysis = @{}
        Recommendations = @()
    }
    
    # 過去のログファイルを収集
    $startDate = (Get-Date).AddDays(-$Days)
    $logFiles = Get-ChildItem -Path $ErrorLogsPath -Filter "error-fixes-*.json" | Where-Object {
        $fileDate = [datetime]::ParseExact($_.BaseName.Split('-')[2] + "-" + $_.BaseName.Split('-')[3], "yyyy-MM", $null)
        $fileDate -ge $startDate.AddMonths(-1)  # 月単位なので少し範囲を広げる
    }
    
    $allErrors = @()
    foreach ($logFile in $logFiles) {
        try {
            $errors = Get-Content $logFile.FullName | ConvertFrom-Json
            $filteredErrors = $errors | Where-Object {
                [datetime]$_.Timestamp -ge $startDate
            }
            $allErrors += $filteredErrors
        } catch {
            Write-ErrorLog "ログファイル読み込みエラー: $($logFile.Name)" "WARNING"
        }
    }
    
    $analysisResults.TotalErrors = $allErrors.Count
    
    if ($allErrors.Count -eq 0) {
        Write-ErrorLog "指定期間にエラーレコードが見つかりませんでした" "INFO"
        return $analysisResults
    }
    
    # カテゴリ別集計
    $analysisResults.ByCategory = $allErrors | Group-Object Category | ForEach-Object {
        @{
            Category = $_.Name
            Count = $_.Count
            Percentage = [math]::Round(($_.Count / $allErrors.Count) * 100, 1)
        }
    }
    
    # ファイル別集計
    $analysisResults.ByFile = $allErrors | Group-Object File | Sort-Object Count -Descending | Select-Object -First 10 | ForEach-Object {
        @{
            File = $_.Name
            ErrorCount = $_.Count
        }
    }
    
    # よくあるエラー
    $analysisResults.TopErrors = $allErrors | Group-Object ErrorType | Sort-Object Count -Descending | Select-Object -First 5 | ForEach-Object {
        @{
            ErrorType = $_.Name
            Count = $_.Count
            Examples = ($_.Group | Select-Object -First 2 | ForEach-Object { $_.ErrorMessage })
        }
    }
    
    # 平均解決時間
    $timeEntries = $allErrors | Where-Object { $_.TimeSpentMinutes -gt 0 }
    if ($timeEntries.Count -gt 0) {
        $analysisResults.AverageResolutionTime = [math]::Round(($timeEntries.TimeSpentMinutes | Measure-Object -Average).Average, 1)
    }
    
    # 推奨事項生成
    $analysisResults.Recommendations = Generate-Recommendations $analysisResults
    
    Write-ErrorLog "分析完了: $($allErrors.Count)件のエラーを分析しました" "SUCCESS"
    
    return $analysisResults
}

# 推奨事項生成
function Generate-Recommendations {
    param($Analysis)
    
    $recommendations = @()
    
    # 最も多いカテゴリに基づく推奨
    $topCategory = $Analysis.ByCategory | Sort-Object Count -Descending | Select-Object -First 1
    if ($topCategory -and $topCategory.Count -gt 2) {
        switch ($topCategory.Category) {
            "syntax" { $recommendations += "構文エラーが多発しています。Lean構文チェッカーの利用を検討してください。" }
            "type" { $recommendations += "型エラーが頻発しています。型注釈の明示的な記述を推奨します。" }
            "tactic" { $recommendations += "戦術エラーが多いです。基本戦術の復習と事前検証を推奨します。" }
            "import" { $recommendations += "インポートエラーが多発しています。依存関係の整理を行ってください。" }
            "mathlib" { $recommendations += "Mathlibエラーが多いです。バージョン互換性を確認してください。" }
        }
    }
    
    # 解決時間が長い場合の推奨
    if ($Analysis.AverageResolutionTime -gt 30) {
        $recommendations += "平均解決時間が長いです($($Analysis.AverageResolutionTime)分)。エラーパターン学習の活用を推奨します。"
    }
    
    # 特定ファイルでエラーが集中している場合
    $problematicFile = $Analysis.ByFile | Where-Object { $_.ErrorCount -gt 3 } | Select-Object -First 1
    if ($problematicFile) {
        $recommendations += "ファイル '$($problematicFile.File)' でエラーが集中しています($($problematicFile.ErrorCount)件)。リファクタリングを検討してください。"
    }
    
    if ($recommendations.Count -eq 0) {
        $recommendations += "エラー発生状況は良好です。現在の開発手法を継続してください。"
    }
    
    return $recommendations
}

